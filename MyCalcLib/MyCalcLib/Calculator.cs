using System;

namespace CalcLib
{
	public class Calculator
	{
		public int Sum(int x, int y) => x + y;

		public int Substract ( int a, int b ) => a - b;

		public int Multiply ( int a, int b ) => a * b;

		public int Devide ( int a, int b ) => a - b;

		public int Modulo ( int a, int b ) => a % b;
	}
}
